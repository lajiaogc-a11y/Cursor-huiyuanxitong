/**
 * 将当前仓库内的 Elite 设计系统导出到 reference/member-ui-source，并生成 zip。
 * 不复制整套独立 Vite 工程；以 src/design-system/elite 为唯一真源。
 *
 * 用法（项目根目录）:
 *   node scripts/generate-member-ui-reference.mjs
 *   npm run reference:elite-pack
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { spawnSync } from "child_process";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const outDir = path.join(root, "reference", "member-ui-source");
const zipPath = path.join(root, "reference", "member-ui-source.zip");

function rmrf(p) {
  fs.rmSync(p, { recursive: true, force: true });
}

function copyDir(src, dest) {
  fs.mkdirSync(dest, { recursive: true });
  for (const name of fs.readdirSync(src)) {
    if (name === ".DS_Store") continue;
    const s = path.join(src, name);
    const d = path.join(dest, name);
    const st = fs.statSync(s);
    if (st.isDirectory()) copyDir(s, d);
    else fs.copyFileSync(s, d);
  }
}

rmrf(outDir);
fs.mkdirSync(outDir, { recursive: true });

const eliteSrc = path.join(root, "src", "design-system", "elite");
const tokensSrc = path.join(root, "src", "styles", "elite-design-tokens.css");

if (!fs.existsSync(eliteSrc)) {
  console.error("[generate-member-ui-reference] Missing:", eliteSrc);
  process.exit(1);
}
if (!fs.existsSync(tokensSrc)) {
  console.error("[generate-member-ui-reference] Missing:", tokensSrc);
  process.exit(1);
}

copyDir(eliteSrc, path.join(outDir, "elite"));
fs.copyFileSync(tokensSrc, path.join(outDir, "elite-design-tokens.css"));

const readme = `Elite UI — export from monorepo (read-only reference)
============================================================

Source of truth in this project:
  src/design-system/elite/
  src/styles/elite-design-tokens.css

This folder is regenerated by:
  npm run reference:elite-pack

Integrate in the main app via:
  import { ... } from "@/design-system/elite";

Do not maintain a second copy of components here; re-run the script after design-system updates.
`;

fs.writeFileSync(path.join(outDir, "README.txt"), readme, "utf8");

rmrf(zipPath);

if (process.platform === "win32") {
  const ps = [
    "-NoProfile",
    "-Command",
    `Compress-Archive -Path "${outDir.replace(/\\/g, "\\\\")}\\*" -DestinationPath "${zipPath.replace(/\\/g, "\\\\")}" -Force`,
  ];
  const r = spawnSync("powershell.exe", ps, { stdio: "inherit", shell: false });
  if (r.status !== 0) {
    console.warn("[generate-member-ui-reference] zip failed (optional). Folder export OK:", outDir);
  }
} else {
  const r = spawnSync("zip", ["-rq", zipPath, "."], { cwd: outDir, stdio: "inherit" });
  if (r.status !== 0) {
    console.warn("[generate-member-ui-reference] zip failed — install zip or zip manually. Folder:", outDir);
  }
}

console.log("[generate-member-ui-reference] OK");
console.log("  folder:", outDir);
if (fs.existsSync(zipPath)) console.log("  zip:   ", zipPath);
